/**
 * op_add - add two integer
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substract two integer
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply two integer
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two integer
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - get the modulo of two integer
 * @a: 1st integer
 * @b: 2nd integer
 *
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
